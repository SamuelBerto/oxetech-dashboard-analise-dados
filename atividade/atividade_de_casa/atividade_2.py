# Atividade 2: Operadores Lógicos

print(True and False)
print(True or False)
print(not False)

# Em Python, True e False devem começar com letra maiúscula.
# Se estiverem minúsculos, o Python entenderá como variáveis.

# Regra do "and":
# Ambas as condições precisam ser verdadeiras.

# Exemplos:
# True and True = True
# True and False = False
# False and True = False
# False and False = False

# Regra do "or":
# Apenas uma condição precisa ser verdadeira.

# Exemplos:
# True or True = True
# True or False = True
# False or True = True
# False or False = False

# Regra do "not":
# Inverte o valor lógico.

# Exemplos:
# not True = False
# not False = True
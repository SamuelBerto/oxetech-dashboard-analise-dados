#int() converte o valor digitado para número inteiro
numero_inteiro = int(input("Digite um número inteiro: "))

numero_inteiro_2 = int(input("Digite o segundo número inteiro: "))

#Exibindo os resultados das operações matemáticas na tela
print(f"Soma: {numero_inteiro + numero_inteiro_2}")
print(f"Subtração: {numero_inteiro - numero_inteiro_2}")
print(f"Multiplicação: {numero_inteiro * numero_inteiro_2}")
print(f"Divisão: {numero_inteiro / numero_inteiro_2}")
                    
